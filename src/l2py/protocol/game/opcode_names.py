def packet_name(opcode: int, data: bytes | None = None) -> str | None:
    """Return PascalCase packet name for a primary opcode.

    If ``opcode == 0xFE`` (the ``ExPacket`` family) and ``data`` begins with a
    2-byte LE sub-opcode, returns the extended packet name. Otherwise returns
    the primary name or ``None`` if unknown.
    """
    if opcode == 0xFE and data is not None and len(data) >= 2:
        sub = data[0] | (data[1] << 8)
        ex_name = EX_OPCODE_NAMES.get(sub)
        if ex_name is not None:
            return ex_name
    return OPCODE_NAMES.get(opcode)


